@app.route('/register', methods=['POST', 'GET'])
def register():
    register_form = RegisterForm()
    if register_form.validate_on_submit():
        new_user = User()
        data = register_form.data
        # Check user in db
        if User.query.filter_by(email=data["email"]).first():
            flash("You're already registered with this email. Login instead")
            return redirect("login")
        for field in data:
            if field != "password":
                setattr(new_user, field, data[field])
            else:
                new_user.password = generate_password_hash(register_form.password.data, method='pbkdf2:sha256',
                                                           salt_length=8)
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for('get_all_posts'))
    return render_template("register.html", form=register_form)


@app.route('/login', methods=['POST', 'GET'])
def login():
    login_form = LoginForm()
    if login_form.validate_on_submit():
        # print(login_form.data)
        user = User.query.filter_by(email=login_form.email.data).first()
        if not user:
            flash("User not recognized. Please try again")
            return redirect(url_for('login'))
        elif not check_password_hash(user.password, login_form.password.data):
            flash("The password you typed seems to be wrong. Please try again")
            return redirect(url_for('login'))
        else:
            login_user(user)
            flash('Logged in successfully.')
            return redirect(url_for('get_all_posts'))
    return render_template("login.html", form=login_form)


